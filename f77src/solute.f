C
C     MONOCALCIUM PHOSPHATE
C
      P1=ACA1
      P2=AH2PB
      P1=AMAX1(ZERO,P1)
      P2=AMAX1(ZERO,P2)
      SPX=SPCAM 
      S0=P1+P2
      S1=AMAX1(0.0,S0**2-4.0*(P1*P2-SPX))
      RPCMBX=AMAX1(-PCAPMB,TPDX*(S0-SQRT(S1)))
      ELSE
      RPALBX=0.0
      RPFEBX=0.0
      RPCDBX=0.0
      RPCHBX=0.0
      RPCMBX=0.0
      RHA0B1=0.0
      RHA1B1=0.0
