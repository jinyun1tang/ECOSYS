C
      PX=AMAX1(AH1PB,AH2PB)
      R1=AHY1
      P1=ACA1
      IF(PX.EQ.AH1PB)THEN
      P2=AH1PB
      NR1=0
      SP=SPCAD 
      ELSEIF(PX.EQ.AH2PB)THEN
      P2=AH2PB
      NR1=1
      SP=SHCAD2 
      ENDIF
      RPCDB1=0.0
      RHCDB2=0.0
      R1=AMAX1(ZERO,R1)
      P1=AMAX1(ZERO,P1)
      P2=AMAX1(ZERO,P2)
      SPX=SP*R1**NR1 
      S0=P1+P2
      S1=AMAX1(0.0,S0**2-4.0*(P1*P2-SPX))
      RPCDBX=AMAX1(-PCAPDB,TPDX*(S0-SQRT(S1)))
      IF(PX.EQ.AH1PB)THEN
      RPCDB1=RPCDBX
      ELSEIF(PX.EQ.AH2PB)THEN
      RHCDB2=RPCDBX
      ENDIF
C
C     HYDROXYAPATITE
C
      PX=AMAX1(AH1PB,AH2PB)
      R1=AHY1
      P1=ACA1
      IF(PX.EQ.AH1PB)THEN
      P2=AH1PB
      NR1=4
      SP=SHCAH1 
      ELSEIF(PX.EQ.AH2PB)THEN
      P2=AH2PB
      NR1=7
      SP=SHCAH2 
      ENDIF
      RHCHB1=0.0
      RHCHB2=0.0
      R1=AMAX1(ZERO,R1)
