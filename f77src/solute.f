      P1=AMAX1(ZERO,P1)
      P2=AMAX1(ZERO,P2)
      P3=AMAX1(ZERO,P3)
      SPX=SP*R1**NR1/P3**NP3
      S0=P1+P2
      S1=AMAX1(0.0,S0**2-4.0*(P1*P2-SPX))
      RPFEBX=AMAX1(-PFEPOB,TPDX*(S0-SQRT(S1)))
      IF(PY.EQ.AH1PB)THEN
      IF(PX.EQ.AFE1)THEN
      RHF0B1=RPFEBX
      ELSEIF(PX.EQ.AFEO1)THEN
      RHF1B1=RPFEBX
      ELSEIF(PX.EQ.AFEO2)THEN
      RHF2B1=RPFEBX
      ELSEIF(PX.EQ.AFEO3)THEN
      RHF3B1=RPFEBX
      ELSEIF(PX.EQ.AFEO4)THEN
      RHF4B1=RPFEBX
      ENDIF
      ELSE
      IF(PX.EQ.AFE1)THEN
      RHF0B2=RPFEBX
      ELSEIF(PX.EQ.AFEO1)THEN
      RHF1B2=RPFEBX
      ELSEIF(PX.EQ.AFEO2)THEN
      RHF2B2=RPFEBX
      ELSEIF(PX.EQ.AFEO3)THEN
      RHF3B2=RPFEBX
      ELSEIF(PX.EQ.AFEO4)THEN
      RHF4B2=RPFEBX
