      RHA1B2=RPALBX
      ELSEIF(PX.EQ.AALO2)THEN
      RHA2B2=RPALBX
      ELSEIF(PX.EQ.AALO3)THEN
      RHA3B2=RPALBX
      ELSEIF(PX.EQ.AALO4)THEN
      RHA4B2=RPALBX
      ENDIF
      ENDIF
